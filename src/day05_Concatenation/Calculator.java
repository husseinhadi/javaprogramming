package day05_Concatenation;

public class Calculator {
    public static void main(String[] args) {

        int firstName = 100;
        int secondNumber = 50;

        int addition = firstName + secondNumber;
        int subtraction = firstName - secondNumber;


        // 100 + 50 = 150
        //100 - 50 = 50
        // 100 * 50 = 5000


        System.out.println(firstName + " + " + secondNumber + " = " + (addition) ) ;
        System.out.println(firstName + " - " + secondNumber + " = " + (subtraction) ) ;

    }
}
