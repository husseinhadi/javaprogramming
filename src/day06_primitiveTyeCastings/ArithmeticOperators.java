package day06_primitiveTyeCastings;

public class ArithmeticOperators {

    public static void main(String[] args) {
        System.out.println("12" + 1); // 121, concatenation

        System.out.println(10 + 20); // 30, addition

    }


    /*
    + : Addition
    - : subtract
    * : multiplication
    / : division
    % :  remainder
     */



}
